#include "ft_printf.h"

int	ft_putstr(char *c)
{
	int	len;

	len = 0;
	if (c == NULL)
		c = "(null)";
	while (*c)
	{
		write(1, c, 1);
		c++;
		len++;
	}
	return (len);
}
